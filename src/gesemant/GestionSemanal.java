    public void totalHorasHombreDisponible(){
        int time1 = Integer.parseInt((String) comienzaHora.getSelectedItem());
        int time2 = Integer.parseInt((String) terminaHora.getSelectedItem());
        
        String type1 = (String) tComienzaHora.getSelectedItem();
        String type2 = (String) tTerminaHora.getSelectedItem();
        
        if(type1 == "pm" && time1 != 12)
            time1 = time1+12;
        
        if(type2 == "pm" && time2 != 12)
            time2 = time2+12;

        SimpleDateFormat format = new SimpleDateFormat("H");
        Date d1 = null;
        Date d2 = null;
		
        try {
            d1 = format.parse(""+time1);
            d2 = format.parse(""+time2);

            //in milliseconds
            long diff = d2.getTime() - d1.getTime();

           // long diffMinutes = diff / (60 * 1000) % 60;
            diffHours = diff / (60 * 60 * 1000) % 24;

            if(diffHours < 0){
                comienzaHora.setSelectedIndex(7);
                terminaHora.setSelectedIndex(3);
                tComienzaHora.setSelectedIndex(0);
                tTerminaHora.setSelectedIndex(1);
                JOptionPane.showMessageDialog(null, "La hora de comienzo no puede ser menor que la hora de terminar!!"); 
            }
            else{
                nHorasJornada.setText(""+diffHours);
            }
            
            if(!nTrabajadores.getText().isEmpty() && !nDiasLaborables.getText().isEmpty() && !nHorasJornada.getText().isEmpty()){
                int trabajadores = Integer.valueOf(nTrabajadores.getText());
                int dias = Integer.valueOf(nDiasLaborables.getText());
                int horas =  Integer.valueOf(nHorasJornada.getText());

                totalHorasHombre.setText(""+trabajadores*(dias*horas));
            }
            else{
                totalHorasHombre.setText("0");
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        } 
    }
    
